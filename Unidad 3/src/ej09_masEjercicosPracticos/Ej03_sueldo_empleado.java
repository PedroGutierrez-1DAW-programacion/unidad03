package ej09_masEjercicosPracticos;

public class Ej03_sueldo_empleado {
	private String nombre;
	private int sueldo;

	Ej03_sueldo_empleado(String nombre, int sueldo){
		this.nombre = nombre;
		this.sueldo = sueldo;
	}

	public String getNombre() {
		return nombre;
	}
	public int getSueldo() {
		return sueldo;
	}
	
}
