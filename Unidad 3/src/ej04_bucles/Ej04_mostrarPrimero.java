package ej04_bucles;

import java.util.Scanner;

public class Ej04_mostrarPrimero {

	public static void main(String[] args) {		
		
		Scanner teclado = new Scanner(System.in);
		int n;
		
		n = teclado.nextInt();
		if(n>=0) while(teclado.nextInt()>=0);
			
		System.out.println(n);
		
		teclado.close();

	}

}
